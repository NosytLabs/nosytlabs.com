#!/usr/bin/env node

/**
 * Master Build Optimizer for NosytLabs - 2025
 * Consolidates all optimization scripts into a single, efficient system
 * Replaces: unified-optimizer.js, optimize-bundles.js, performance-seo-optimization.js,
 *          comprehensive-redundancy-elimination.js, final-redundancy-cleanup.js
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');

class MasterBuildOptimizer {
  constructor() {
    this.stats = {
      filesProcessed: 0,
      duplicatesRemoved: 0,
      originalSize: 0,
      optimizedSize: 0,
      bundlesCreated: 0,
      cssOptimized: 0,
      jsOptimized: 0,
      redundanciesFixed: 0
    };
    
    this.config = {
      // CSS Bundle Configuration
      cssBundles: {
        'critical.css': {
          description: 'Critical above-the-fold styles',
          files: ['src/styles/css-variables.css', 'src/styles/global.css'],
          inline: true
        },
        'main.css': {
          description: 'Main application styles',
          files: ['src/styles/nosytlabs.css', 'src/styles/consolidated-styles.css']
        },
        'win95.css': {
          description: 'Windows 95 theme styles',
          files: ['public/styles/win95-authentic.css', 'public/styles/ms-sans-serif.css']
        }
      },
      
      // JavaScript Bundle Configuration
      jsBundles: {
        'core.min.js': {
          description: 'Core functionality',
          files: ['public/scripts/core/main.js', 'public/scripts/core/utils.js'],
          priority: 'high'
        },
        'nosytos95.min.js': {
          description: 'NosytOS95 functionality',
          files: ['public/scripts/nosytos95/*.js'],
          priority: 'low'
        }
      },
      
      // Files to remove (duplicates and redundant files)
      redundantFiles: [
        'scripts/optimize-bundles.js',
        'scripts/unified-optimizer.js',
        'scripts/performance-seo-optimization.js',
        'scripts/comprehensive-redundancy-elimination.js',
        'scripts/final-redundancy-cleanup.js',
        'public/styles/consolidated-styles.css', // Duplicate of src version
        'dist/styles/consolidated-styles.css'    // Duplicate of src version
      ]
    };
  }

  log(message, type = 'info') {
    const timestamp = new Date().toISOString().replace('T', ' ').substring(0, 19);
    const icons = { info: '🔄', success: '✅', warning: '⚠️', error: '❌' };
    console.log(`[${timestamp}] ${icons[type]} ${message}`);
  }

  async optimize() {
    this.log('Starting Master Build Optimization', 'info');
    console.log('🚀 Consolidating all optimization processes...\n');
    
    try {
      // Phase 1: Remove redundant files
      await this.removeRedundantFiles();
      
      // Phase 2: Optimize CSS bundles
      await this.optimizeCSS();
      
      // Phase 3: Optimize JavaScript bundles
      await this.optimizeJavaScript();
      
      // Phase 4: Update build configuration
      await this.updateBuildConfig();
      
      // Phase 5: Generate performance utilities
      await this.generatePerformanceUtils();
      
      // Phase 6: Update package.json scripts
      await this.updatePackageScripts();
      
      // Phase 7: Generate comprehensive report
      this.generateReport();
      
      this.log('Master optimization completed successfully!', 'success');
      
    } catch (error) {
      this.log(`Optimization failed: ${error.message}`, 'error');
      throw error;
    }
  }

  async removeRedundantFiles() {
    this.log('Phase 1: Removing redundant files');
    
    for (const filePath of this.config.redundantFiles) {
      const fullPath = path.join(rootDir, filePath);
      
      if (fs.existsSync(fullPath)) {
        try {
          const stats = fs.statSync(fullPath);
          fs.unlinkSync(fullPath);
          this.stats.redundanciesFixed++;
          this.stats.originalSize += stats.size;
          this.log(`  Removed: ${filePath}`, 'success');
        } catch (error) {
          this.log(`  Failed to remove: ${filePath} - ${error.message}`, 'warning');
        }
      }
    }
  }

  async optimizeCSS() {
    this.log('Phase 2: Optimizing CSS bundles');
    
    const outputDir = path.join(rootDir, 'public/styles/bundles');
    this.ensureDirectory(outputDir);
    
    for (const [bundleName, config] of Object.entries(this.config.cssBundles)) {
      await this.createCSSBundle(bundleName, config, outputDir);
    }
  }

  async createCSSBundle(bundleName, config, outputDir) {
    let consolidatedCSS = `/* ${config.description} - Generated by Master Build Optimizer */\n\n`;
    let bundleSize = 0;
    let filesProcessed = 0;
    
    for (const filePath of config.files) {
      const fullPath = path.join(rootDir, filePath);
      
      if (fs.existsSync(fullPath)) {
        const content = fs.readFileSync(fullPath, 'utf8');
        const originalSize = content.length;
        
        consolidatedCSS += `/* ===== ${path.basename(filePath)} ===== */\n`;
        consolidatedCSS += content;
        consolidatedCSS += '\n\n';
        
        bundleSize += originalSize;
        filesProcessed++;
        
        this.log(`  Added: ${filePath} (${(originalSize / 1024).toFixed(2)} KB)`);
      }
    }
    
    if (filesProcessed > 0) {
      // Optimize CSS content
      const optimizedCSS = this.optimizeCSSContent(consolidatedCSS);
      const outputPath = path.join(outputDir, bundleName);
      
      fs.writeFileSync(outputPath, optimizedCSS);
      
      this.stats.bundlesCreated++;
      this.stats.cssOptimized++;
      this.stats.optimizedSize += optimizedCSS.length;
      
      const compressionRatio = ((bundleSize - optimizedCSS.length) / bundleSize * 100).toFixed(1);
      this.log(`  Bundle created: ${bundleName} (${filesProcessed} files, ${(optimizedCSS.length / 1024).toFixed(2)} KB, ${compressionRatio}% compressed)`, 'success');
    }
  }

  optimizeCSSContent(css) {
    // Remove comments (except important ones)
    css = css.replace(/\/\*(?!\!)[^*]*\*+(?:[^/*][^*]*\*+)*\//g, '');
    
    // Remove excessive whitespace
    css = css.replace(/\s+/g, ' ');
    css = css.replace(/;\s*}/g, '}');
    css = css.replace(/{\s*/g, '{');
    css = css.replace(/;\s*/g, ';');
    
    // Remove empty rules
    css = css.replace(/[^}]*{\s*}/g, '');
    
    return css.trim();
  }

  async optimizeJavaScript() {
    this.log('Phase 3: Optimizing JavaScript bundles');
    
    const outputDir = path.join(rootDir, 'public/scripts/bundles');
    this.ensureDirectory(outputDir);
    
    for (const [bundleName, config] of Object.entries(this.config.jsBundles)) {
      await this.createJSBundle(bundleName, config, outputDir);
    }
  }

  async createJSBundle(bundleName, config, outputDir) {
    let consolidatedJS = `/* ${config.description} - Generated by Master Build Optimizer */\n\n`;
    let bundleSize = 0;
    let filesProcessed = 0;
    
    for (const filePath of config.files) {
      const fullPath = path.join(rootDir, filePath);
      
      if (fs.existsSync(fullPath)) {
        const content = fs.readFileSync(fullPath, 'utf8');
        const originalSize = content.length;
        
        consolidatedJS += `/* ===== ${path.basename(filePath)} ===== */\n`;
        consolidatedJS += content;
        consolidatedJS += '\n\n';
        
        bundleSize += originalSize;
        filesProcessed++;
        
        this.log(`  Added: ${filePath} (${(originalSize / 1024).toFixed(2)} KB)`);
      }
    }
    
    if (filesProcessed > 0) {
      const outputPath = path.join(outputDir, bundleName);
      fs.writeFileSync(outputPath, consolidatedJS);
      
      this.stats.bundlesCreated++;
      this.stats.jsOptimized++;
      this.stats.optimizedSize += consolidatedJS.length;
      
      this.log(`  Bundle created: ${bundleName} (${filesProcessed} files, ${(consolidatedJS.length / 1024).toFixed(2)} KB)`, 'success');
    }
  }

  ensureDirectory(dirPath) {
    if (!fs.existsSync(dirPath)) {
      fs.mkdirSync(dirPath, { recursive: true });
    }
  }

  async updateBuildConfig() {
    this.log('Phase 4: Updating build configuration');
    
    // Update Astro config with optimized settings
    const configPath = path.join(rootDir, 'astro.config.optimized.mjs');
    const optimizedConfig = this.generateOptimizedAstroConfig();
    
    fs.writeFileSync(configPath, optimizedConfig);
    this.log('  Optimized Astro config generated', 'success');
  }

  generateOptimizedAstroConfig() {
    return `// Optimized Astro Configuration - Generated by Master Build Optimizer
import { defineConfig } from 'astro/config';
import react from '@astrojs/react';
import tailwind from '@astrojs/tailwind';

export default defineConfig({
  integrations: [
    react(),
    tailwind({ config: { applyBaseStyles: false } })
  ],
  output: 'static',
  build: {
    format: 'file',
    inlineStylesheets: 'auto',
    assets: 'assets',
    assetsPrefix: process.env.NODE_ENV === 'production' ? 'https://cdn.nosytlabs.com' : undefined
  },
  vite: {
    build: {
      minify: 'terser',
      cssMinify: 'lightningcss',
      cssCodeSplit: true,
      assetsInlineLimit: 2048,
      sourcemap: false,
      target: ['es2020', 'chrome80', 'firefox78', 'safari14'],
      rollupOptions: {
        output: {
          manualChunks: {
            'react-vendor': ['react', 'react-dom'],
            'analytics': ['@vercel/analytics', '@vercel/speed-insights']
          },
          assetFileNames: 'assets/[name]-[hash][extname]',
          chunkFileNames: 'chunks/[name]-[hash].js',
          entryFileNames: 'entries/[name]-[hash].js'
        }
      }
    },
    optimizeDeps: {
      include: ['react', 'react-dom'],
      exclude: ['@vercel/analytics', '@vercel/speed-insights']
    }
  }
});`;
  }

  async generatePerformanceUtils() {
    this.log('Phase 5: Generating performance utilities');

    const performanceUtils = `/**
 * Performance Optimization Utilities - Generated by Master Build Optimizer
 * Consolidated performance enhancements for NosytLabs website
 */

// Lazy loading utility
export function setupLazyLoading() {
  if ('IntersectionObserver' in window) {
    const imageObserver = new IntersectionObserver((entries, observer) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          const img = entry.target;
          img.src = img.dataset.src || img.src;
          img.classList.remove('lazy');
          observer.unobserve(img);
        }
      });
    });

    document.querySelectorAll('img[data-src]').forEach(img => {
      imageObserver.observe(img);
    });
  }
}

// Critical resource preloading
export function preloadCriticalResources() {
  const criticalResources = [
    { href: '/styles/bundles/critical.css', as: 'style' },
    { href: '/scripts/bundles/core.min.js', as: 'script' }
  ];

  criticalResources.forEach(resource => {
    const link = document.createElement('link');
    link.rel = 'preload';
    link.href = resource.href;
    link.as = resource.as;
    document.head.appendChild(link);
  });
}

// Performance monitoring
export function monitorPerformance() {
  if ('performance' in window) {
    window.addEventListener('load', () => {
      setTimeout(() => {
        const perfData = performance.getEntriesByType('navigation')[0];
        const metrics = {
          dns: perfData.domainLookupEnd - perfData.domainLookupStart,
          tcp: perfData.connectEnd - perfData.connectStart,
          ttfb: perfData.responseStart - perfData.requestStart,
          download: perfData.responseEnd - perfData.responseStart,
          dom: perfData.domContentLoadedEventEnd - perfData.domContentLoadedEventStart,
          load: perfData.loadEventEnd - perfData.loadEventStart
        };

        console.log('Performance Metrics:', metrics);
      }, 0);
    });
  }
}

// Initialize all performance optimizations
export function initPerformanceOptimizations() {
  setupLazyLoading();
  preloadCriticalResources();
  monitorPerformance();
}`;

    const utilsPath = path.join(rootDir, 'src/utils/performance-optimized.ts');
    fs.writeFileSync(utilsPath, performanceUtils);
    this.log('  Performance utilities generated', 'success');
  }

  async updatePackageScripts() {
    this.log('Phase 6: Updating package.json scripts');

    const packagePath = path.join(rootDir, 'package.json');
    const packageData = JSON.parse(fs.readFileSync(packagePath, 'utf8'));

    // Update scripts to use the master optimizer
    packageData.scripts['optimize:master'] = 'node scripts/master-build-optimizer.js';
    packageData.scripts['build:optimized'] = 'npm run optimize:master && astro build';

    // Remove old optimization scripts
    delete packageData.scripts['optimize:all'];
    delete packageData.scripts['optimize'];

    fs.writeFileSync(packagePath, JSON.stringify(packageData, null, 2));
    this.log('  Package.json scripts updated', 'success');
  }

  generateReport() {
    this.log('Phase 7: Generating optimization report');

    const savings = this.stats.originalSize - this.stats.optimizedSize;
    const savingsPercent = this.stats.originalSize > 0 ?
      ((savings / this.stats.originalSize) * 100).toFixed(1) : '0';

    console.log('\n📊 Master Build Optimization Report');
    console.log('=====================================');
    console.log(`Files Processed: ${this.stats.filesProcessed}`);
    console.log(`Bundles Created: ${this.stats.bundlesCreated}`);
    console.log(`CSS Files Optimized: ${this.stats.cssOptimized}`);
    console.log(`JS Files Optimized: ${this.stats.jsOptimized}`);
    console.log(`Redundancies Fixed: ${this.stats.redundanciesFixed}`);
    console.log(`Original Size: ${this.formatBytes(this.stats.originalSize)}`);
    console.log(`Optimized Size: ${this.formatBytes(this.stats.optimizedSize)}`);
    console.log(`Size Reduction: ${this.formatBytes(savings)} (${savingsPercent}%)`);

    console.log('\n💡 Optimization Summary:');
    console.log('✅ Redundant files removed');
    console.log('✅ CSS bundles optimized');
    console.log('✅ JavaScript bundles created');
    console.log('✅ Build configuration updated');
    console.log('✅ Performance utilities generated');
    console.log('✅ Package scripts consolidated');

    console.log('\n🚀 Next Steps:');
    console.log('1. Use "npm run build:optimized" for production builds');
    console.log('2. Test the optimized bundles in development');
    console.log('3. Monitor performance metrics after deployment');
    console.log('4. Consider implementing service worker caching');
  }

  formatBytes(bytes) {
    if (bytes === 0) return '0 Bytes';
    const k = 1024;
    const sizes = ['Bytes', 'KB', 'MB', 'GB'];
    const i = Math.floor(Math.log(bytes) / Math.log(k));
    return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i];
  }
}

// Run the master optimizer
console.log('Starting Master Build Optimizer...');
const optimizer = new MasterBuildOptimizer();
console.log('Optimizer created, starting optimization...');
optimizer.optimize().catch(error => {
  console.error('Optimization failed:', error);
  process.exit(1);
});
