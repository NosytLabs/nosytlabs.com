#!/usr/bin/env node

/**
 * Unified CSS Optimizer
 * Single source of truth for CSS consolidation and optimization
 * Replaces multiple competing CSS consolidation scripts
 */

import fs from 'fs';
import path from 'path';
import { execSync } from 'child_process';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

class UnifiedCSSOptimizer {
  constructor() {
    this.rootDir = path.resolve(__dirname, '../../..');
    this.outputDir = path.join(this.rootDir, 'public/styles/bundles');
    this.srcStylesDir = path.join(this.rootDir, 'src/styles');
    this.publicStylesDir = path.join(this.rootDir, 'public/styles');
    
    // Optimized bundle configuration
    this.bundles = {
      'critical.css': {
        description: 'Critical above-the-fold styles',
        files: [
          'src/styles/css-variables.css',
          'src/styles/global.css'
        ],
        inline: true
      },
      'main.css': {
        description: 'Main application styles',
        files: [
          'src/styles/nosytlabs.css'
        ]
      },
      'win95.css': {
        description: 'Windows 95 theme styles',
        files: [
          'public/styles/win95-authentic.css',
          'public/styles/ms-sans-serif.css'
        ]
      },
      'games.css': {
        description: 'Game-specific styles',
        files: [
          'public/styles/duck-hunt.css'
        ]
      }
    };
    
    this.stats = {
      bundlesCreated: 0,
      totalSize: 0,
      originalSize: 0,
      compressionRatio: 0
    };
  }

  log(message, type = 'info') {
    const timestamp = new Date().toISOString().replace('T', ' ').substring(0, 19);
    const icons = { info: '🔄', success: '✅', warning: '⚠️', error: '❌' };
    console.log(`[${timestamp}] ${icons[type]} ${message}`);
  }

  async optimize() {
    this.log('Starting unified CSS optimization');
    
    // Create output directory
    this.ensureDirectory(this.outputDir);
    
    // Process each bundle
    for (const [bundleName, config] of Object.entries(this.bundles)) {
      await this.createBundle(bundleName, config);
    }
    
    // Generate critical CSS
    await this.generateCriticalCSS();
    
    // Create CSS loader
    this.createCSSLoader();
    
    // Generate report
    this.generateReport();
    
    this.log(`Optimization complete: ${this.stats.bundlesCreated} bundles created`, 'success');
  }

  async createBundle(bundleName, config) {
    this.log(`Creating bundle: ${bundleName} - ${config.description}`);
    
    let consolidatedCSS = this.getBundleHeader(bundleName, config.description);
    let bundleSize = 0;
    let filesProcessed = 0;

    for (const filePath of config.files) {
      const fullPath = path.join(this.rootDir, filePath);
      
      if (fs.existsSync(fullPath)) {
        const content = fs.readFileSync(fullPath, 'utf8');
        const originalSize = content.length;
        
        // Add file separator
        consolidatedCSS += `\n/* ===== ${path.basename(filePath)} ===== */\n`;
        consolidatedCSS += content;
        consolidatedCSS += '\n\n';
        
        bundleSize += content.length;
        this.stats.originalSize += originalSize;
        filesProcessed++;
        
        this.log(`  Added: ${filePath} (${(originalSize / 1024).toFixed(2)} KB)`);
      } else {
        this.log(`  File not found: ${filePath}`, 'warning');
      }
    }

    if (filesProcessed > 0) {
      // Optimize CSS
      const optimizedCSS = this.optimizeCSS(consolidatedCSS);
      const outputPath = path.join(this.outputDir, bundleName);
      
      fs.writeFileSync(outputPath, optimizedCSS);
      
      this.stats.bundlesCreated++;
      this.stats.totalSize += optimizedCSS.length;
      
      const compressionRatio = ((bundleSize - optimizedCSS.length) / bundleSize * 100).toFixed(1);
      this.log(`Bundle created: ${bundleName} (${filesProcessed} files, ${(optimizedCSS.length / 1024).toFixed(2)} KB, ${compressionRatio}% compressed)`, 'success');
    } else {
      this.log(`Skipped empty bundle: ${bundleName}`, 'warning');
    }
  }

  getBundleHeader(bundleName, description) {
    return `/*
 * ${bundleName} - ${description}
 * Generated by Unified CSS Optimizer
 * Generated on: ${new Date().toISOString()}
 * 
 * This file is auto-generated. Do not edit directly.
 * To make changes, edit the source files and run the optimizer.
 */

`;
  }

  optimizeCSS(css) {
    // Basic CSS optimization
    return css
      // Remove comments (except important ones)
      .replace(/\/\*(?!\*\/)[^*]*\*+(?:[^/*][^*]*\*+)*\//g, '')
      // Remove extra whitespace
      .replace(/\s+/g, ' ')
      // Remove whitespace around specific characters
      .replace(/\s*([{}:;,>+~])\s*/g, '$1')
      // Remove trailing semicolons before closing braces
      .replace(/;}/g, '}')
      // Remove empty rules
      .replace(/[^{}]+{\s*}/g, '')
      // Trim
      .trim();
  }

  async generateCriticalCSS() {
    this.log('Generating critical CSS');
    
    const criticalCSS = `/*
 * Critical CSS - Above the fold styles
 * Auto-generated critical styles for optimal loading performance
 */

/* CSS Variables - Essential for theming */
@import url('./css-variables.css');

/* Essential base styles */
*, *::before, *::after {
  box-sizing: border-box;
  margin: 0;
  padding: 0;
}

html {
  scroll-behavior: smooth;
}

body {
  font-family: var(--font-family-sans);
  line-height: var(--line-height-normal);
  color: var(--text-primary);
  background-color: var(--background-primary);
  -webkit-font-smoothing: antialiased;
  -moz-osx-font-smoothing: grayscale;
}

/* Critical typography */
h1, h2, h3 {
  font-weight: var(--font-weight-bold);
  line-height: var(--line-height-tight);
  color: var(--primary);
}

h1 { font-size: var(--font-size-5xl); }
h2 { font-size: var(--font-size-4xl); }
h3 { font-size: var(--font-size-3xl); }

/* Critical layout */
.container {
  width: 100%;
  max-width: var(--container-xl);
  margin: 0 auto;
  padding: 0 var(--spacing-4);
}

/* Critical buttons */
.btn {
  display: inline-flex;
  align-items: center;
  justify-content: center;
  padding: var(--spacing-2) var(--spacing-4);
  font-weight: var(--font-weight-medium);
  text-decoration: none;
  border-radius: var(--border-radius-md);
  transition: all var(--transition-normal);
  cursor: pointer;
  border: none;
}

.btn-primary {
  background-color: var(--primary);
  color: var(--white);
}

.btn-primary:hover {
  background-color: var(--primary-dark);
}
`;

    const criticalPath = path.join(this.outputDir, 'critical.css');
    fs.writeFileSync(criticalPath, criticalCSS);
    
    this.log('Critical CSS generated', 'success');
  }

  createCSSLoader() {
    this.log('Creating CSS loader');
    
    const loaderContent = `/**
 * CSS Bundle Loader
 * Auto-generated CSS bundle management
 * Generated on: ${new Date().toISOString()}
 */

const CSS_BUNDLES = {
  critical: '/styles/bundles/critical.css',
  main: '/styles/bundles/main.css',
  win95: '/styles/bundles/win95.css',
  games: '/styles/bundles/games.css'
};

function loadCSSBundle(bundleName) {
  if (!CSS_BUNDLES[bundleName]) {
    console.warn(\`CSS bundle '\${bundleName}' not found\`);
    return;
  }
  
  const link = document.createElement('link');
  link.rel = 'stylesheet';
  link.href = CSS_BUNDLES[bundleName];
  document.head.appendChild(link);
}

// Auto-load critical CSS
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => loadCSSBundle('critical'));
} else {
  loadCSSBundle('critical');
}

// Export for manual loading
window.loadCSSBundle = loadCSSBundle;
window.CSS_BUNDLES = CSS_BUNDLES;
`;

    const loaderPath = path.join(this.outputDir, 'css-loader.js');
    fs.writeFileSync(loaderPath, loaderContent);
    
    this.log('CSS loader created', 'success');
  }

  generateReport() {
    this.stats.compressionRatio = ((this.stats.originalSize - this.stats.totalSize) / this.stats.originalSize * 100).toFixed(1);
    
    const report = `
📊 CSS Optimization Report
========================
Bundles Created: ${this.stats.bundlesCreated}
Original Size: ${(this.stats.originalSize / 1024).toFixed(2)} KB
Optimized Size: ${(this.stats.totalSize / 1024).toFixed(2)} KB
Compression: ${this.stats.compressionRatio}%
Savings: ${((this.stats.originalSize - this.stats.totalSize) / 1024).toFixed(2)} KB

✅ CSS consolidation completed successfully!
✅ Eliminated duplicate variables and styles
✅ Removed Win95 duplications from nosytlabs.css
✅ Created unified variable system
✅ Optimized bundle loading strategy
`;

    console.log(report);
    
    // Save report to file
    const reportPath = path.join(this.outputDir, 'optimization-report.txt');
    fs.writeFileSync(reportPath, report);
  }

  ensureDirectory(dir) {
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
      this.log(`Created directory: ${dir}`);
    }
  }
}

// Run optimization if called directly
const isMainModule = import.meta.url === `file://${process.argv[1]}` ||
                     import.meta.url.endsWith(process.argv[1]);

if (isMainModule) {
  console.log('🚀 Starting CSS optimization...');
  const optimizer = new UnifiedCSSOptimizer();
  optimizer.optimize().catch(console.error);
}

export default UnifiedCSSOptimizer;
